import React, { useEffect, useState } from 'react';
import { useNavigate, useLocation } from 'react-router-dom';
import { motion } from 'framer-motion';
import Confetti from 'react-confetti';
import { Trophy, Home, Share2, Check, X } from 'lucide-react';

import html2canvas from 'html2canvas';

const Results = () => {
    const navigate = useNavigate();
    const [windowSize, setWindowSize] = useState({
        width: window.innerWidth,
        height: window.innerHeight,
    });

    useEffect(() => {
        const handleResize = () => {
            setWindowSize({
                width: window.innerWidth,
                height: window.innerHeight,
            });
        };
        window.addEventListener('resize', handleResize);
        return () => window.removeEventListener('resize', handleResize);
    }, []);

    const { state } = useLocation();
    const score = state?.score || 0;
    const userAnswers = state?.userAnswers || [];
    const playerName = state?.playerName || 'Player';

    const handleSaveResult = async () => {
        const element = document.getElementById('quiz-report-card');
        if (!element) return;

        try {
            const canvas = await html2canvas(element, {
                scale: 2, // Higher resolution
                backgroundColor: '#ffffff', // Ensure white background
            });

            const image = canvas.toDataURL('image/png');

            // Trigger download
            const link = document.createElement('a');
            link.href = image;
            link.download = `${playerName}-quiz-result.png`;
            link.click();
        } catch (err) {
            console.error('Failed to save image:', err);
        }
    };

    return (
        <div className="min-h-screen bg-primary flex flex-col items-center justify-center p-4 overflow-hidden relative">
            <Confetti width={windowSize.width} height={windowSize.height} numberOfPieces={300} />

            <motion.div
                initial={{ opacity: 0, y: -50 }}
                animate={{ opacity: 1, y: 0 }}
                className="text-center mb-12 z-10"
            >
                <h1 className="text-5xl md:text-7xl font-black text-white mb-4 drop-shadow-lg">
                    Great Job!
                </h1>
                <p className="text-xl text-white/80">You finished the quiz</p>
            </motion.div>

            <motion.div
                initial={{ scale: 0 }}
                animate={{ scale: 1 }}
                transition={{ type: "spring", stiffness: 200, damping: 20 }}
                className="bg-white rounded-3xl p-12 shadow-2xl mb-12 text-center z-10"
            >
                <div className="text-gray-500 font-bold text-xl mb-2">Your Score</div>
                <div className="text-8xl font-black text-primary mb-4">{score}</div>
                <div className="text-gray-400 font-medium">points</div>
            </motion.div>

            {/* Hidden Report Card for Screenshot */}
            <div id="quiz-report-card" className="absolute top-0 left-0 -z-50 bg-white p-8 w-[600px] text-gray-800">
                <div className="text-center mb-8 border-b pb-4">
                    <h1 className="text-4xl font-bold text-primary mb-2">Quiz Report Card</h1>
                    <h2 className="text-2xl font-bold">{playerName}</h2>
                    <p className="text-xl text-gray-500">Score: {score}</p>
                </div>
                <div className="space-y-4">
                    {userAnswers.map((ans, i) => (
                        <div key={i} className="border-b border-gray-100 pb-4 last:border-0">
                            <p className="font-bold text-lg mb-1">{i + 1}. {ans.questionText}</p>
                            <div className="flex justify-between items-center">
                                <span className={`font-medium ${ans.isCorrect ? 'text-green-600' : 'text-red-500'}`}>
                                    Selected: {ans.selectedOptionText}
                                </span>
                                {ans.isCorrect ? (
                                    <Check className="text-green-500" size={24} />
                                ) : (
                                    <X className="text-red-500" size={24} />
                                )}
                            </div>
                        </div>
                    ))}
                </div>
                <div className="mt-8 text-center text-gray-400 text-sm">
                    Generated by AI Quiz Platform
                </div>
            </div>

            <motion.div
                initial={{ opacity: 0 }}
                animate={{ opacity: 1 }}
                transition={{ delay: 2 }}
                className="flex gap-4 z-10"
            >
                <button
                    onClick={() => navigate('/')}
                    className="px-8 py-4 bg-white text-primary rounded-xl font-bold text-lg hover:bg-gray-100 transition-all flex items-center gap-2 shadow-lg"
                >
                    <Home size={20} /> Back to Home
                </button>
                <button
                    onClick={handleSaveResult}
                    className="px-8 py-4 bg-green-500 text-white rounded-xl font-bold text-lg hover:bg-green-600 transition-all flex items-center gap-2 shadow-lg"
                >
                    <Share2 size={20} /> Save Result
                </button>
            </motion.div>
        </div>
    );
};

export default Results;
